      The Holy Qur’an <span id=uline>encourages</span> the people of this faith to practice peacekeeping and prohibits them from creating “unnecessary enmity” with others, unless in defense from oppressors. However, in contrast to this, misconceptions that link Islam and terrorism perpetuate to this day as verses in the Holy Qur’an <span id=uline>remain</span> vulnerable to the risk of misinterpretation from extremist groups. <br>

      <br><br>

      <span id='heading'><div><b>Moving mountains</b></div></span><br>

      <br><br>

      These misguided notions of violence in the name of Islam have contributed to the severe discrimination against the religion, causing many Muslims to hide the signs of their faith due to feelings of <span id=uline>inferiority</span>. However, in the case of Marawi, the free expression of faith is essential in their recovery. <br>

      <br>

      Faith is among the central forces in post-war rehabilitation initiatives—a "<span id=uline>reconciler</span>" of sorts, as researcher Prince Conteh put it. Further studies have also shown that being able to practice one’s faith, especially after a “major life stressor,” boosts resilience and allows individuals to “achieve fundamental positive transformation.”<br>

<br>

This importance of faith is evidenced in the displaced Maranao’s simple <span id=uline>request</span>: Amidst the need for basic goods, medicine, and shelter, they also ask for cultural and religious understanding from their fellow Filipinos so that they may practice Islam unburdened with discrimination and accusations.<br>

<br>

“More [opportunities for] dialogue [are] needed [between Muslims and Christians] for better understanding of [each other’s] needs,” Adiong said.
Despite the misinterpretations of individuals from within and outside of the faith, Islam is a peaceful religion that has sustained the displaced Maranaos throughout two long years of rehabilitation. The problems of Marawi’s recovery process run deep, but regular Filipino citizens can begin to play their part by practicing cultural and religious sensitivity—the foundations of solidarity, peace, and empathy. <br>
