      <span id=blue>AMONG MOUNTAINS of crushed cement,</span> tangles of overgrown vines, and scatterings of misshapen steel, a silver crescent sits atop the rubble as it furiously glints under the harsh rays of the Mindanao sun.<br>

      <br>

      It has been two years since the <span id=uline>siege</span> that devastated Marawi City. What started out as an Armed Forces of the Philippines (AFP) mission to detain Abu Sayyaf subleader and Maute Group member Isnilon Hapilon led to a five-month battle that displaced nearly <span id=uline>300,000 Maranaos</span> and even residents from nearby municipalities. <br>

      <br>

      But for the Maute Group, the destruction that ensued wasn’t just about protecting the interests of one man—it was also a war for and about religion. Numerous reports have said that the Maute Group targeted not only the AFP, but the <span id=uline>Christian population</span> in Marawi as well. Christians who refused to convert to Islam were reportedly beheaded, a Catholic church was burnt to the ground, and a priest and several Christians who worshipped with him were held hostage for the entirety of the siege.<br>

      <br>

      Perhaps without meaning to, these stories have further reinforced the <span id=uline>misguided notion</span> of Muslim-Christian hostility in the Philippines. In reality, the Maute Group’s acts of violence, which they claim to have done allahu akbar, or in the name of God, also severely hurt their fellow Muslims. Not only were mosques used as battlefields, ancestral homes destroyed, and many killed as casualties of the war, but the siege also added to the already difficult challenge of deconstructing the anti-Muslim narrative.<br>

      <br><br>

    <span id='heading'><div><b>Of the same feather</b></div></span><br>

    <br><br>

    In contrast to the acts of violence perpetrated by the Maute Group, the Holy Qur’an—the <span id=uline>central religious text</span> of Islam—contains several <span id=uline>verses</span> that teach and promote peace, such as Qur’an 6:108 which says, “And do not insult those they invoke other than Allah, lest they insult Allah in enmity without knowledge.” Qur’an 59:23 even <span id=uline>reveals</span> that peace is one of the names of God himself: “He (is) Allah, the One Who, (there is) no god but He, the Sovereign, the Holy One, the Giver of Peace, the Giver of Security, the Guardian, the All-Mighty, the Irresistible, the Supreme. Glory (be to) Allah from what they associate (with Him).”<br>

    <br>

    A manifestation of this adherence to peace was seen in the Marawi siege itself when former Catholic Bishops Conference of the Philippines (CBCP) President Socrates Villegas <span id=uline>recounted</span> narratives of Muslims and Christians protecting each other from harm. <br>

