      Islamic teacher from the Marqaz Mosque in Marawi City Abolkhair Bantuas shared similar sentiments. “Kung baga sa Islam, pantay-pantay—walang mataas at walang mababa… Basta sa Islam, pare-pareho—dapat nagkakaisa (In Islam, we are all equal—no one is ranked above or below the other. In Islam, we are one),” he said about the Christian-Muslim dynamic in Mindanao.<br>

      <br>

      This much is seen in Sarimanok Tent City 2. On the day of <span id=uline>Eid al-Adha</span>, the holier of two internationally celebrated Islamic holidays, Muslim and Christian refugees alike shared in the meat from the cows that were sacrificed as part of the religious feast. Regardless of faith, the residents of the tent city enjoyed equal amounts of beef for dinner.<br>

      <br>

      Still, many have misconstrued the violent acts perpetrated by the Maute Group during the Marawi siege to represent the beliefs and dispositions of the entire Muslim population.<br>

      <br>

      In an interview with The GUIDON, founder of the International Relations and Islamic Studies Research Cohort at the University of the Philippines Diliman Nassef Manabilang Adiong, PhD pointed out the lack of communication between Christians and Muslims as one of the barriers to understanding the two interconnected religions. “We don’t have a lot of opportunities for dialogue where we can discuss our issues together,” he said. <br>

      <br><br>

    <span id='heading'><div><b>Deconstructing Islamophobia</b></div></span><br>

    <br><br>

      The negative perceptions about the Muslim faith have been historically widespread—so much so that the term Islamophobia was coined in the <span id=uline>1990s</span> to encompass such attitudes. More specifically, <span id=uline>Islamophobia</span> refers to the fear or anxiety that is associated with the religion of Islam or its believers. Much of this fear has to do with uninformed opinions and negative stereotypes that resulted from <span id=uline>political propaganda</span> against the rise of Islam. Over the years, the manifestations of these negative attitudes toward Muslims have gradually <span id=uline>escalated</span> from avoidance and othering to outright violence.<br>

      <br>

      However, what truly <span id=uline>aggravated</span> Islamophobia in the West, thus influencing their fear to bleed out into nations near and far, is the infamous 9/11 terrorist attack in 2001. Among the most remembered facts of the case is the involvement of an Islamic terrorist group called Al-Qaeda. Al-Qaeda’s coordinated attack <span id=uline>left</span> approximately 2,977 people dead. <br>

      <br>

      Following this extreme act of violence was the erroneous association of terrorism with the Islamic faith. The Islamophobia of the 1990s took on a different, more aggressive tone—one that persists to this very day. In fact, the <span id=uline>latest attack</span> on the Muslim population happened on March 2019 in Christchurch, New Zealand, wherein a gunman shot 51 Muslims during Friday prayers at two mosques. The incident has since been <span id=uline>labeled</span> as America’s “worst peace-time mass shooting.”<br>

      <br><br>

    <span id='heading'><div><b>Wayward son</b></div></span><br>

    <br><br>

      In the local context, the manifestations of Islamophobia are not as violent, but the fear is <span id=uline>present</span>, nonetheless. Like their Western counterparts, many Filipinos have cited the existence of Islamic <span id=uline>militant</span> groups as the source of their wariness with the country’s Muslim population.<br>

      <br>

      

      <br>

      However, Adiong calls on the Filipino people, especially those who live in Manila and are more generally removed from Muslim Mindanao, to temper such judgments. "We don’t hold the right to label [Muslims], whether [they] are extreme or moderate," he explained.<br>
